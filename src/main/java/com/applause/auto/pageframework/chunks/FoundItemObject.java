package com.applause.auto.pageframework.chunks;

public class FoundItemObject {
	private String name;
	private int amountOfStars;
	private int amountOfVoted;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAmountOfStars() {
		return amountOfStars;
	}

	public void setAmountOfStars(int amountOfStars) {
		this.amountOfStars = amountOfStars;
	}

	public int getAmountOfVoted() {
		return amountOfVoted;
	}

	public void setAmountOfVoted(int amountOfVoted) {
		this.amountOfVoted = amountOfVoted;
	}
}
